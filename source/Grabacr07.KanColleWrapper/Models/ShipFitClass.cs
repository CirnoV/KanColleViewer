		public static int GetFit(int ShipId, int SlotitemId)
		{
			if (!FitData.ContainsKey(ShipId)) return 0;

			var fit = FitData[ShipId].FirstOrDefault(x => x.Slotitem == SlotitemId);
			if (fit == null) return 0;

			return Math.Min(fit.Day, fit.Night);
		}
